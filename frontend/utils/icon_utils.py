def render_icon(name: str, color: str = "#2c4733", size: int = 24, inline: bool = True) -> str:
    """
    Render a Lucide icon as inline HTML/SVG

    Args:
        name: Icon name from LUCIDE_ICONS
        color: Hex color for the icon (default: Flourish green)
        size: Size in pixels (default: 24)
        inline: If True, renders inline with text, otherwise as block

    Returns:
        HTML string with embedded SVG icon
    """
    if name not in LUCIDE_ICONS:
        return ""

    svg = LUCIDE_ICONS[name]

    # Replace width, height, and stroke color
    svg = svg.replace('width="24"', f'width="{size}"')
    svg = svg.replace('height="24"', f'height="{size}"')
    svg = svg.replace('stroke="currentColor"', f'stroke="{color}"')

    # Wrap in span for inline display
    if inline:
        return f'<span style="display: inline-block; vertical-align: middle; margin-right: 8px;">{svg}</span>'
    else:
        return svg


def get_page_icon(page_name: str, color: str = "#3a5a44", size: int = 32) -> str:
    """
    Get the appropriate icon for a page

    Args:
        page_name: Name of the page (home, students, trends, review, targets)
        color: Icon color
        size: Icon size

    Returns:
        HTML string with icon
    """
    icon_map = {
        "home": "sprout",
        "students": "users",
        "trends": "trending-up",
        "review": "clipboard-check",
        "targets": "target",
    }

    icon_name = icon_map.get(page_name.lower(), "leaf")
    return render_icon(icon_name, color=color, size=size, inline=False)


def render_icon_with_text(icon_name: str, text: str, color: str = "#3a5a44", size: int = 24) -> str:
    """
    Render an icon with text next to it

    Args:
        icon_name: Name of the icon
        text: Text to display
        color: Icon color
        size: Icon size

    Returns:
        HTML string with icon and text
    """
    icon_html = render_icon(icon_name, color=color, size=size, inline=True)
    return f'{icon_html}<span style="vertical-align: middle;">{text}</span>'
